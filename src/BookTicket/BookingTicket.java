    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        ShowTimeDAO stdao = new ShowTimeDAO();
        TicketDAO tdao = new TicketDAO();
        
        int indexMovie = cbMovies.getSelectedIndex();
        int indexDate = cbDate.getSelectedIndex();
        int indexTime = cbTime.getSelectedIndex();
        int indexRoom = cbRoom.getSelectedIndex();
        
        if (indexDate==0 || indexMovie==0 || indexRoom==0 || indexTime==0) return;
        else
        {
            try
            {
                Date dateShow = listShowDate.get(indexDate-1).getDateShow();
                int movieID = listMovieIdAndName.get(indexMovie-1).getId();
                int roomID = listRoomID.get(indexRoom-1).getRoomID();
                Time startTime = listTime.get(indexTime-1).getStartTime();
                Time endTime = listTime.get(indexTime-1).getEndTime();

                int showTimeID = stdao.getShowTimeID(dateShow, movieID, startTime, endTime, roomID, ticketPrice);
                String staffID = lbUsername.getText().trim();

                boolean purchase = false;
                for (String seatID : listSeatID) {
                    TicketDTO ticketDTO = new TicketDTO();
                    ticketDTO.setStaffID(staffID);
                    ticketDTO.setShowTimeID(showTimeID);
                    ticketDTO.setSeatID(seatID);

                    purchase = tdao.purchase(ticketDTO);
                }

                if (purchase)
                    {
                        for (int i = 0; i < 5; i++) {
                            for (int j = 1; j < 7; j++) {
                                if (seat[i][j].getBackground()==Color.RED)
                                {
                                    seat[i][j].setEnabled(false);
                                }
                            }
                        }
                        JOptionPane.showMessageDialog(null, "Purchase Success");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Purchase Failed!!!");
                    }
            } catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage());
                e.printStackTrace();
            }
        }
        
        
    }//GEN-LAST:event_btnPurchaseActionPerformed

