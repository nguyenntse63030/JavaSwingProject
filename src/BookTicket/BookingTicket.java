    private void cbRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbRoomActionPerformed
        ShowTimeDAO stdao = new ShowTimeDAO();
        TicketDAO tdao = new TicketDAO();
        int indexMovie = cbMovies.getSelectedIndex();
        int indexDate = cbDate.getSelectedIndex();
        int indexTime = cbTime.getSelectedIndex();
        int indexRoom = cbRoom.getSelectedIndex();
        
        if (indexRoom == 0) return;
        else
        {
            try
            {
                Date dateShow = listShowDate.get(indexDate-1).getDateShow();
                int movieID = listMovieIdAndName.get(indexMovie-1).getId();
                int roomID = listRoomID.get(indexRoom-1).getRoomID();
                Time startTime = listTime.get(indexTime-1).getStartTime();
                Time endTime = listTime.get(indexTime-1).getEndTime();
                int Price = stdao.getPrice(dateShow, movieID, startTime, endTime, roomID);
                ticketPrice = Price;
                
                int showTimeID = stdao.getShowTimeID(dateShow, movieID, startTime, endTime, roomID, ticketPrice);
                ArrayList<TicketDTO> listSeat = (ArrayList<TicketDTO>) tdao.findByPrimaryKey(showTimeID);
                
                for (int i = 0; i < 5; i++) {
                    for (int j = 1; j < 7; j++) {
                        seat[i][j].setTicketPrice(ticketPrice);
                        seat[i][j].setBackground(new JButton().getBackground());
                        seat[i][j].setEnabled(true);
                        
                        for (TicketDTO ticketDTO : listSeat) {
                            if (seat[i][j].getID().equals(ticketDTO.getSeatID()))
                            {
                                seat[i][j].setEnabled(false);
                            }
                        }
                    }
                }
                
                listSeatID.clear();
                txtTicket.setText("0");
                txtTotal.setText("0");
            } catch (Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_cbRoomActionPerformed

