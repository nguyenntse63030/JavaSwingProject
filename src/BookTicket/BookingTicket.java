    public void insertSeatToPanelSeat()
    {
        try
        {
        for (int i = 'A'; i <= 'E'; i++) {
            for (int j = 1; j <= 6; j++) {
                seat[i-'A'][j] = new Seat(Character.toString((char) i) + j, txtTicket,txtTotal);
                seat[i-'A'][j].setList(listSeatID);
                pnSeat.add(seat[i-'A'][j]);
                seat[i-'A'][j].setEnabled(false);
            }
        }
        } catch (Exception e)
        {
            e.printStackTrace();
        }
    }
    
    private void disableSeatButton()
    {
        for (int i = 0; i < 5; i++) {
            for (int j = 1; j < 7; j++) {
                seat[i][j].setEnabled(false);
            }
        }
    }
    
